    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        if(txtLotteryName.getText().length() != 0 && txtLotteryCodes.getText().length() != 0 && txtNumOfBalls.getText().length() != 0 ) {

            try {
                connection = DBConnection.getConnection();
                stmt = connection.createStatement();

                //String query="INSERT INTO `lottery_details`( `lottery_name`, `lottery_code`, `num_of_balls`, `num_of_lettrs`, `num_of_zodiac_signs`, `num_of_fate_no`,`num_of_specialNo`,`ent_user`) VALUES (?,?,?,?,?,?,?,?)";

                String query = "INSERT INTO `tbl_lottery_det`(`brand_name`, `brand_code`, `number_of_balls`, `symbols`, `fate_no`, `special_no`, `enter_user`,`lot_status`) VALUES (?,?,?,?,?,?,?,?)";
                ps = connection.prepareStatement(query);
                ps.setString(1, txtLotteryName.getText());
                ps.setString(2, txtLotteryCodes.getText());
                ps.setInt(3, Integer.parseInt(txtNumOfBalls.getText()));
                ps.setString(4, symbols.getSelectedItem().toString());
                ps.setString(5, fateNumber.getSelectedItem().toString());
                ps.setString(6, specialNumber.getSelectedItem().toString());
                ps.setString(7, "Anuruddhika");
                ps.setString(8, cStatus.getSelectedItem().toString());
                ps.executeUpdate();


                String tableName = "CREATE TABLE " + txtLotteryCodes.getText() + "_combinations" + "("
                        + "rec_count INT NOT NULL AUTO_INCREMENT PRIMARY KEY,"
                        + "draw_no INT(64),"
                        + "no1 INT(2),"
                        + "no2 INT(2),"
                        + "no3 INT(2),"
                        + "no4 INT(2),"
                        + "eng_letter VARCHAR(1),"
                        + "book_no INT(64),"
                        + "ent_date TIMESTAMP,"
                        + "ent_user VARCHAR(50),"
                        + "order_status VARCHAR(20))";

                System.out.println("NAME : " + tableName);
                stmt.executeUpdate(tableName);

                String prize_count_tbl = "CREATE TABLE " + txtLotteryCodes.getText() + "_prize_count" +
                        "(book_no INT(11), rec_count bigint(21), draw_no INT(11))";
                stmt.executeUpdate(prize_count_tbl);


                String prize_det_tbl = "CREATE TABLE " + txtLotteryCodes.getText() + "_prize_det" +
                        "(book_no INT(11), rec_no INT(11), prize_code VARCHAR(10), draw_no INT(11))";
                stmt.executeUpdate(prize_det_tbl);

                String winning_det_tbl = "CREATE TABLE " + txtLotteryCodes.getText() + "_winning_details" +
                        "(draw_no INT(11),no1 INT(11),no2 INT(11),no3 INT(11),no4 INT(11),eng_letter VARCHAR(1))";
                stmt.executeUpdate(winning_det_tbl);
                JOptionPane.showMessageDialog(this, "Lottery created Successfully.");


                //UI UPDATES
                DefaultTableModel model=(DefaultTableModel)tableLotteryBrands.getModel();
                int c=model.getRowCount();
                int r_val=0;
                if(c > 0)
                r_val=Integer.parseInt(model.getValueAt(c-1,0).toString());

                model.addRow(new Object[]{++r_val,txtLotteryName.getText(),txtLotteryCodes.getText(), txtNumOfBalls.getText(),symbols.getSelectedItem().toString(),fateNumber.getSelectedItem().toString(),specialNumber.getSelectedItem().toString(),cStatus.getSelectedItem().toString()});
                txtLotteryName.setText("");
                txtLotteryCodes.setText("");
                txtNumOfBalls.setText("");
                symbols.setSelectedIndex(0);
                fateNumber.setSelectedIndex(0);
                specialNumber.setSelectedIndex(0);
                cStatus.setSelectedIndex(0);

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

        }else{
            JOptionPane.showMessageDialog(this, "Please Enter Lottery Details");
        }

    }//GEN-LAST:event_btnSaveActionPerformed

