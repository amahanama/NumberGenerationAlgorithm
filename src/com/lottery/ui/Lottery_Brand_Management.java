    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel d1=(DefaultTableModel) tableLotteryBrands.getModel();
        int selectedIndex=tableLotteryBrands.getSelectedRow();
        
        int id =Integer.parseInt(d1.getValueAt(selectedIndex, 0).toString());
        System.out.println("ID >>> "+id);
        System.out.println("ROW ID >>> "+row_id);

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        System.out.println(dtf.format(now));

        d1.removeRow(row_id);
        d1.addRow(new Object[]{id,txtLotteryName.getText(),txtLotteryCodes.getText(), txtNumOfBalls.getText(),symbols.getSelectedItem().toString(),fateNumber.getSelectedItem(),specialNumber.getSelectedItem().toString(),cStatus.getSelectedItem().toString()});

        try {
            connection= DBConnection.getConnection();
            stmt=connection.createStatement();


            String query="UPDATE `tbl_lottery_det` SET `number_of_balls`="+Integer.parseInt(txtNumOfBalls.getText())+",`symbols`='"+symbols.getSelectedItem().toString()+"',`fate_no`='"+fateNumber.getSelectedItem().toString()+"',`special_no`='"+specialNumber.getSelectedItem().toString()+"',`mod_date`='"+dtf.format(now)+"',`mod_user`='ANURUDDHIKA',`lot_status`='"+cStatus.getSelectedItem().toString()+"' WHERE lot_no="+id;

            System.out.println("QUERY >>> "+query);
            ps = connection.prepareStatement(query);
            ps.executeUpdate();


        txtLotteryName.setText("");
        txtLotteryCodes.setText("");
        txtNumOfBalls.setText("");        
        cStatus.setSelectedIndex(0);
        tableLotteryBrands.setAutoCreateRowSorter(true);
        tableLotteryBrands.getRowSorter().toggleSortOrder(0);
        JOptionPane.showMessageDialog(this, "Lottery updated successfully.");
    }catch(Exception ex){
      ex.printStackTrace();
        }
    }

